"""Формирование бланка справочников для завуча (markdown)."""
from __future__ import annotations

from collections import defaultdict
from pathlib import Path


def write_markdown_guide(data: dict, out_path: Path) -> None:
    class_by_key = {c["display"]: c for c in data["classes"]}
    curriculum_by_class: dict[str, list] = defaultdict(list)
    for row in data["curriculum"]:
        curriculum_by_class[row["class"]].append(row)

    lines = [
        "# Бланк справочников (до составления расписания)",
        "",
        f"Школа: **{data['school_hint']}**. Источник: прошлогодний файл `{data['source']}` — ",
        "личные расписания педагогов за одну неделю. **Сетки уроков здесь нет** — только то, ",
        "что завуч заносит в справочники **до** раскладки.",
        "",
        "**Как пользоваться:** идите по разделам 1→8 и переносите в АРМ Завуча → Справочники. ",
        "Колонки «заполнить» — то, чего в старом файле не было. ",
        "Часы нагрузки — по будням (пн–пт), **без дублей** из личных расписаний классных ",
        "руководителей (считается только предметник). ",
        "Строка «Литература 5 М … _5ИЛМ классы» = **1 час для 5М**, а не для И+Л+М. ",
        "Сверьте с приказом о нагрузке.",
        "",
        "---",
        "",
        "## 1. Здания",
        "",
        "| № | Название в АРМ | Откуда взято |",
        "|:---:|---|---|",
    ]
    for i, b in enumerate(data["buildings"], 1):
        note = {
            "Основное здание": "основной корпус",
            "Начальная школа «Белочка»": "здание 8, ул. Пионерская",
            "Флигель": "отдельный корпус",
        }.get(b, "")
        lines.append(f"| {i} | {b} | {note} |")

    lines += [
        "",
        "### Переходы между зданиями (заполнить)",
        "",
        "Минуты на переход. Пусто = переход не задан.",
        "",
        "| От \\ До | "
        + " | ".join(data["buildings"])
        + " |",
        "|---|" + "|".join(["---"] * len(data["buildings"])) + "|",
    ]
    for from_b in data["buildings"]:
        lines.append("| " + from_b + " | " + " | ".join(["…"] * len(data["buildings"])) + " |")

    lines += [
        "",
        "---",
        "",
        f"## 2. Кабинеты ({len(data['rooms'])})",
        "",
        "| Кабинет | Здание | Вместимость (заполнить) | Закреплённый учитель (заполнить) |",
        "|---|---|---:|---|",
    ]
    for r in data["rooms"]:
        lines.append(f"| {r['number']} | {r['building']} | | |")

    lines += [
        "",
        "---",
        "",
        f"## 3. Предметы ({len(data['subjects'])})",
        "",
        "Занести названия; балл Сивкова подставится из шаблона Минпросвещения.",
        "",
    ]
    for i, s in enumerate(data["subjects"], 1):
        lines.append(f"{i}. {s}")

    lines += [
        "",
        "---",
        "",
        f"## 4. Классы ({len(data['classes'])})",
        "",
        "| Класс | Параллель | Буква | Смена | Учеников (заполнить) | Кл. руководитель (заполнить) |",
        "|---|---:|---|---:|---:|---|",
    ]
    for c in data["classes"]:
        lines.append(
            f"| {c['display']} | {c['grade']} | {c['letter']} | {c['shift']} | | |"
        )

    lines += [
        "",
        "---",
        "",
        "## 5. Нагрузка по классам",
        "",
        "Сколько **часов в неделю** у каждого класса по каждому предмету. ",
        "«Кто вёл» — подсказка из прошлого года (для привязки в анкете педагога).",
        "",
    ]
    sorted_classes = sorted(
        curriculum_by_class.keys(),
        key=lambda k: (class_by_key.get(k, {"grade": 99})["grade"], k),
    )
    for cl in sorted_classes:
        meta = class_by_key.get(cl, {"grade": "?", "letter": "?", "shift": "?"})
        rows = curriculum_by_class[cl]
        total = sum(r["hours_per_week"] for r in rows)
        lines += [
            f"### Класс {cl} (параллель {meta['grade']}, смена {meta['shift']})",
            "",
            f"Итого **{total}** ч/нед по {len(rows)} предметам.",
            "",
            "| Предмет | ч/нед | Подгруппы | Кто вёл (подсказка) |",
            "|---|---:|:---:|---|",
        ]
        for r in sorted(rows, key=lambda x: x["subject"]):
            lines.append(
                f"| {r['subject']} | {r['hours_per_week']} | {r['subgroups']} | {r['teacher_hint']} |"
            )
        lines.append("")

    lines += [
        "---",
        "",
        f"## 6. Педагоги ({len(data['teachers'])})",
        "",
        "| № | ФИО | Тип | Основной предмет | Уроков/нед (факт) | Макс. нагрузка (заполнить) | Кл. руководство (заполнить) |",
        "|:---:|---|---|---|---:|---:|---|",
    ]
    for i, ts in enumerate(data["teacher_subjects"], 1):
        top = (
            max(ts["subjects"].items(), key=lambda x: x[1])[0]
            if ts["subjects"]
            else "—"
        )
        ttype = "Предметник"
        if ts["subjects"] and ts["lessons_per_week"] >= 20 and top in (
            "Математика",
            "Русский язык",
            "Литература",
            "Окружающий мир",
        ):
            ttype = "Начальные классы"
        if not ts["subjects"]:
            ttype = "—"
        lines.append(
            f"| {i} | {ts['name']} | {ttype} | {top} | {ts['lessons_per_week']} | 18 | |"
        )

    lines += [
        "",
        "После занесения: в анкете каждого педагога отметьте **галочками строки нагрузки** из раздела 5.",
        "",
        "---",
        "",
        "## 7. Звонки",
        "",
        "Типовое **начало** урока (самое частое в файле). **Конец** — допишите по приказу школы.",
        "",
        "| Шаблон | Смена | Урок | Начало | Конец (заполнить) |",
        "|---|---:|---:|---|---|",
    ]
    for b in data["bells"]:
        lines.append(
            f"| {b['template']} | {b['shift']} | {b['lesson']} | {b['start']} | |"
        )

    lines += [
        "",
        "---",
        "",
        "## 8. Чек-лист перед конструктором",
        "",
        "- [ ] Здания и переходы",
        "- [ ] Кабинеты",
        "- [ ] Предметы",
        "- [ ] Классы (численность, классные руководители)",
        "- [ ] Нагрузка по всем классам",
        "- [ ] Педагоги и привязка нагрузки в анкетах",
        "- [ ] Звонки (обе смены)",
        "",
        "### На что обратить внимание",
        "",
        "- Часы могут отличаться от **учебного плана** — сверьте с приказом о нагрузке.",
        "- Объединённые уроки (_5ИЛМ, _7ИМ и т.д.) — в файле отдельная строка на каждый ",
        "класс (5 И, 5 Л, 5 М); часы не суммируются по буквам суффикса.",
        "- Классы **2-й смены** (5В, 5Г, 6А–6Г, 7А–7Д и др.) в играфике педагогов ",
        "часто указаны только по «своим» предметам — полная нагрузка может быть не видна.",
        "",
        "*(Опционально: те же данные в `Справочники-из-расписания.xlsx` для кнопки «Импорт из Excel».)*",
    ]
    out_path.write_text("\n".join(lines), encoding="utf-8")
